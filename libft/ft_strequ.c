#include "libft.h"

int	ft_strequ(char const *s1, char const *s2)
{
	int				i;
	unsigned char	b;

	if (s1 == NULL || s2 == NULL)
		return (0);
	i = 0;
	while (s1[i] == s2[i] && s1[i] != '\0' && s2[i] != '\0')
		i++;
	b = (unsigned char)s1[i] - (unsigned char)s2[i];
	if (b == 0)
		return (1);
	else
		return (0);
}
