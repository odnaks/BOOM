#include "libft.h"

char	*ft_strnset(char *s, char c, int n)
{
	int	i;

	i = 0;
	if (s == NULL)
		return (NULL);
	while (s[i] != '\0' && i < n)
	{
		s[i] = c;
		i++;
	}
	return (s);
}
