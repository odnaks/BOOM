#include "libft.h"

char			*ft_strdup(const char *str)
{
	char	*newstr;
	int		i;

	i = 0;
	if (!(newstr = (char*)malloc((ft_strlen(str) + 1) * sizeof(char))))
		return (NULL);
	while (str[i] != '\0')
	{
		newstr[i] = str[i];
		i++;
	}
	newstr[i] = '\0';
	return (newstr);
}
